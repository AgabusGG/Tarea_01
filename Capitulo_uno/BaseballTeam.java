
package Capitulo_uno;

public class BaseballTeam {
    Integer equipo;
    Integer bat;
    Integer pelota;
    
    public void setEquipo(Integer equipo){
        this.equipo = equipo;
    }
    public Integer getEquipo(){
        return this.equipo;
    }
    public void setBat(Integer bat){
        this.bat = bat;
    }
    public Integer getBat(){
        return this.bat;
    }
    public void setPelota(Integer pelota){
        this.pelota = pelota;
    }
    public Integer getPelota(){
        return this.pelota;
    }  
}
